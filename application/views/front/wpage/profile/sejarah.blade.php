            <div class="post-content-body">
                <div>
                    <h4>A. ABSTRAKSI</h4>
                    <p>Pondok pesantren PPAI Darun Najah merupakan lembaga sosial publik yang berada di bawah naungan Yayasan Ma’had Darun Najah as-Salafiy, yang didirikan dengan tujuan utama untuk menjaga moral umat dan mencetak kader-kader dakwah Islam di kalangan masyarakat. Didirikan oleh KH. Achmad Muchtar Ghozali, pesantren ini telah berdiri sejak tahun 1967. Hingga kini pesantren ini masih diasuh dibawah bimbingan beliau langsung dan sebagai Ketua Dewan Pembina Yayasan Ma’had Darun Najah as-Salafy Pondok Pesantren Salafiyah PPAI Darun Najah.</p>
                    <p>Pada awalnya pesantren ini berdiri di tepi jalan utama. Tapi karena faktor keterbatasan lahan untuk pengembangan bangunan, pesantren ini kemudian dipindah ke dalam kampung yang bersebelahan dengan areal persawahan yang lebih luas pada tahun 1973. Hingga kini, pesantren ini berdiri dan dan mempunyai lahan seluas 2 hektar dengan persekitaran masyarakat agraris.</p>
                    <p>Karakter utama lembaga pendidikan pesantren ini adalah berkarakter salafiyah, dimana pendidikan agama menjadi prioritas utama dan kitab kuning sebagai rujukan utama. Dengan karakter ini pesantren PPAI Darun Najah mempunyai misi utama pada aspek pengawalan terhadap moralitas masyarakat dan moralitas bangsa, serta menyebarkan nilai-nilai ajaran salafuna as-sholihun dengan sebaik-baiknya sesuai dengan cita-cita al-Quran dan Sunnah Rasulullah Saw. Dan misi tersebut telah terbukti dengan lahirnya banyak alumni-alumni dari pesantren ini yang menjadi agen-agen dakwah Islamiyah yang tersebar di berbagai kota di penjuru nusantara.</p>
                    <p>PPAI merupakan tafa’ulan (nama untuk mengikuti keberkahan) dari pesantren guru beliau, Romo KH. Mohammad Said Anwar di desa Ketapang kec. Kepanjen kab. Malang. Sedangkan Darun Najah merupakan identitas dengan harapan bahwa pesantren ini akan menjadi rumah keselamatan dan kesuksesan (dar : rumah, najah : keselamatan dan kesuksesan)</p>
                    <p>Seiring dengan perkembangan dan tuntutan zaman, Pondok pesantren Salafiyah PPAI Darun Najah memandang perlu untuk berupaya mengintegrasikan pendidikan salafiyah dengan pendidikan formal dan mengkombinasikannya dengan pendidikan ketrampilan yang mencukupi bagi santri dengan diselenggarakannya Pendidikan Layanan Khusus agar pendidikan yang diselenggarakan di dalamnya bisa diakui bukan hanya oleh masyarakat, tetapi juga pemerintah.</p>
                    <p>Pengintegrasian tersebut bertujuan untuk membentuk santri yang memiliki 3 kekuatan, yaitu:</p>
                    <ol>
                        <li>Akhlakul Karimah sebagai kekuatan moral spiritual untuk mencetak santri menjadi insan kamil sesuai dengan cita-cita Islam.</li>
                        <li>Kemampuan keagamaan yang mumpuni untuk menyiapkan santri sebagai agen-agen dakwah Islamiyah.</li>
                        <li>Kemampuan akademis untuk menyiapkan santri agar mampu menghadapi berbagai tantangan modernitas.</li>
                        <li>Kecakapan ketrampilan untuk membekali santri kemampuan dalam menghadapi persaingan di era globalisasi.</li>
                    </ol>
                    <p>Secara ringkas kombinasi selaras antara  pendidikan salafiyah, pendidikan formal dan ketrampilan yang memadai memiliki orientasi untuk menyiapkan santri agar mampu mensinergikan antara kepentingan dunia dan akhirat secara seimbang di era modern bagi semua santri. </p>
                    <p>Untuk tujuan itulah Yayasan Mahad Darun Najah as-Salafy Pondok Pesantren Salafiyah PPAI Darun Najah mulai mengembangkan pendidikan formal untuk membekali santri kemampuan intelektual dengan didirikannya Madrasah Tsanawiyah formal pada tahun 2011, Pendidikan Layanan Khusus pada tahun 2012 dan Madrasah Aliyah formal pada tahun 2013.</p>
                    <p>Dengan kelengkapan lembaga pendidikan tersebut, semua santri tetap diwajibkan untuk tinggal di dalam asrama pesantren untuk menjamin pendidikan bisa dilaksanakan secara integral, menjamin kualitas keilmuan dan keimanan, pendidikan akhlakul karimah secara maksimal, kualitas ketrampilan life skill yang mumpuni dan lebih penting lagi bisa menjamin keamanan bagi santri, khususnya pengaruh negatif dari luar.</p>
                    <p>Hingga kini Pondok Pesantren Salafiyah PPAI Darun Najah telah meluluskan ribuan alumni yang terikat dalam organisasi Ikatan Santri dan Alumni Darun Najah (IKSADA). Dan mayoritas dari para alumni ini telah mampu menjadi kader-kader dakwah Islam yang tersebar bukan hanya di Jawa Timur seperti di Malang, Batu, Pasuruan, Sidoarjo, Madiun, dl; tetapi juga di luar Jawa seperti di Subang, Lampung, Bengkulu, dan Balikpapan.</p>
                </div>
                <div>
                    <h4>B. VISI DAN MISI PESANTREN</h4>
                    <p style="text-align:center">Visi yang diemban oleh Pondok Pesantren Salafiyah PPAI Darun Najah adalah: <br></p>
                    <h5 style="text-align:center"><b>Menjadi Lembaga Pendidikan Islam Berhaluan Ahlus Sunnah Wal Jamaah Yang Unggul dalam Rangka Mencetak Kader Dakwah Yang Berakhlakul Karimah, Berpengetahuan,Terampil dan Bermanfaat.</b></h5>
                    <p>Adapun misi yang akan dijalankan di lembaga Pondok Pesantren Salafiyah PPAI Darun Najah antara lain adalah:</p>
                    <ol>
                        <li>Menyelenggarakan pendidikan diniyah berbasis pada akhlakul karimah dan ajaran salafuna as-sholihun (kitab kuning) secara aplikatif.</li>
                        <li>Menyelenggarakan pendidikan formal berbasis pesantren salafiyah yang profesional.</li>
                        <li>Menyelenggarakan pendidikan layanan berbasis ketrampilan life skill.</li>
                        <li>Menyelenggarakan kegiatan sosial yang bermanfaat bagi masyarakat.</li>
                    </ol>
                </div>
