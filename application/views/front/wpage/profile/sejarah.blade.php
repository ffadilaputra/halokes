                    <ol>
                        <li>Ma’hadiyah</li>
                            <ul>Taman Pendidikan Al-Quran</ul>
                            <ul>Madrasah I’dadiyah Diniyah</ul>
                            <ul>Madrasah Tsanawiyah Diniyah</ul>
                            <ul>Madrasah Aliyah Diniyah</ul>
                            <ul>Pengajian kitab kuning (sorogan dan bandongan)</ul>
                        <li>Madrosiyah</li>
