                    <h4>Sejarah</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Madrasah Tsanawiyah (MTs) Darun Najah adalah lembaga yang berada di bawah naungan Yayasan Ma’had Darun Najah As-Salafy, Pondok Pesantren Salafiyah PPAI Darun Najah yang didirikan sebagai alternatif pendidikan terbaik untuk membentengi umat dari serangan dampak negatif modernisasi dan globalisasi. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Secara sekilas latar belakang didirikannya Madrasah Tsanawiyah Darun Najah adalah desakan masyarakat, khususnya wali santri yang sudah memondokkan puteranya di Darun Najah yang terpaksa menyekolahkan formal putera-puterinya di luar pondok mengingat pendidikan dasar 9 tahun adalah kewajiban dan ijazah formal adalah kebutuhan. Kondisi itu tentu bisa membawa dampak yang kurang baik dan juga beresiko ketika santri kurang pengawasan ketika melaksanakan pembelajaran di luar pondok pesantren. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Hingga atas desakan masyarakat, KH. Achmad Muchtar Ghozali selaku pengasuh memandang perlu untuk mengakomodir aspirasi masyarakat itu dengan mendirikan Madrasah Tsanawiyah Darun Najah yang terintegrasi dengan pendidikan diniyah yang sudah ada dan juga pondok pesantren Darun Najah yang menjadi kekuatannnya. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Didirikan pertama kali pada tahun 2011, hingga kini MTs Darun Najah telah melahirkan ratusan alumni yang bukan hanya mumpuni dalam mata pelajaran umum saja, tapi juga mampu membaca kitab kuning dan ketrampilan dasar lifeskill yang cukup.
                    </p>
                    <br>

                    <h4>Visi Dan Misi Madrasah</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Visi yang diemban oleh Madrasah Tsanawiyah (MTs) Darun Najah adalah:
                    </p>
                    <p style="text-align:center"><b>“Menyelenggaraan Pendidikan berbasis Pesantren yang Profesional dalam Rangka Membentuk Generasi yang Berakhlakul Karimah, Cerdas, Terampil dan Berdaya saing”</b></p>
                    <br>

                    <h4>Misi Madrasah</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Adapun misi Madrasah Tsanawiyah Darun Najah dapat dijabarkan sebagaimana berikut:
                        <ol>
                            <li>Menyelenggarakan pendidikan berdasarkan kurikulum 2013 (Kurtilas).</li>
                            <li>Menyelenggarakan pendidikan dengan berbasis pada akhlakul karimah dan ajaran salafuna as-sholihun khas pesantren Salafiyah (kitab kuning).</li>
                            <li>Mengintegrasikan kurikulum nasional dan kurikulum pesantren secara terpadu.</li>
                            <li>Mengupayakan kemampuan akademis bagi santri yang terstandar nasional dan kecakapan keterampilan secara aplikatif.</li>
                            <li>Menyelenggarakan pendidikan dan pengembangan SDM dengan berbasis pada manajemen berbasis madrasah (MBM).</li>
                            <li>Meningkatkan standar ketuntasan belajar dan kelulusan santri.</li>
                        </ol>

                    </p>
                    <br>

                    <h4>Kurikulum</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pondok pesantren Salafiyah PPAI Darun Najah yang didirikan pada tahun 1967 oleh KH. Achmad Muchtar Ghozali, nyata telah terbukti melahirkan banyak kader umat dan ulama yang mampu mengemban dakwah dan perjuangan Islam di tengah masyarakat. Namun seiring dengan perkembangan dan tuntutan zaman, Pondok Pesantren PPAI Darun Najah memandang perlu untuk berupaya mengintegrasikan pendidikan salafiyah yang menjadi kekuatannya dengan pendidikan nasional (formal) dan mengkombinasikannya dengan pendidikan ketrampilan yang mencukupi. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pengintegrasian tersebut tidak lain bertujuan supaya pendidikan salafiyah (agama) tidak ditinggalkan oleh umat dan agar masyarakat tidak lepas kendali dari kontrol agama Islam. Kombinasi yang selaras antara  pendidikan salafiyah, pendidikan formal dan ketrampilan yang memadai memiliki orientasi pada kepentingan dunia dan akhirat secara seimbang yang akan dicapai oleh santri. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dengan demikian Madrasah Tsanawiyah Darun Najah merupakan madrasah yang mengkombinasikan antara kurikulum Salafiyah, kurikulum kemenag dan juga kurikulum negara dalam kurikulum yang dibangun dengan komposisi 30;30;40. Kurikulum salafiyah dengan menggunakan kitab kuning khas pesantren, kurikulum agama dengan menggunakan khas pengetahuan agama, dan kurikulum negara dengan menguatkan kurikulum Unas yang baik dan mumpuni. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dengan sistem tersebut, semua santri diwajibkan untuk tinggal di dalam asrama pesantren untuk menjamin pendidikan bisa dilaksanakan secara integral, menjamin kualitas keilmuan dan keimanan (akhlakul karimah) secara maksimal, dan lebih penting lagi bisa menjamin keamanan bagi santri, khususnya pengaruh negatif dari luar.
                    </p>
                    <br>

                    <h4>Keunggulan</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Keunggulan yang dimiliki oleh MTs Darun Najah Kabupaten Malang antara lain:
                        <ol>
                            <li>Pelajaran agama semua berbasis kitab kuning dengan capaian kemampuan dan penguasaan kitab kuning khas pondok pesantren Salafiyah.</li>
                            <li>Kurikulum agama Islam dengan menggunakan metode pesantren salafiyah (ngesahi dengan sorogan dan bandongan).</li>
                            <li>Pembelajaran aqidah, akhlak dan fiqih secara aplikatif dan terpadu.</li>
                            <li>Kemampuan tartil al-Quran dan hafalan surat-surat juz Amma.</li>
                            <li>Semua pelajaran, baik umum maupun agama dengan menggunakan sistem kurikulum 2013 (Kurtilas).</li>
                            <li>Upaya pencapaian Kriteria Ketuntasan Minimal (KKM) secara maksimal dalam pembelajaran.</li>
                            <li>Kegiatan berbasis pada life skill agribisnis dan pertukangan untuk santri putra, serta tata boga dan tata busana untuk santri putri.</li>
                            <li>Kegiatan spiritual: pembacaan surat Yasin, Kahfi, Ratibul Haddad, Hizb Nawawi, Maulid Diba’ dan Maulid al-Habsyi.</li>
                        </ol>
                    </p>
                    <br>

                    <h4>Fasilitas</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Fasilitas yang dimiliki oleh MTs Darun Najah demi mendukung penyelenggaraan pendidikan antara lain:
                        <ol>
                            <li>Ruang kelas yang representatif.</li>
                            <li>Asrama bagi santri putra dan putri.</li>
                            <li>Lingkungan pondok pesantren yang asri dan agamis.</li>
                            <li>Laboratorium Komputer.</li>
                            <li>Tempat kegiatan olah raga yang memadai. </li>
                            <li>Perpustakaan yang lengkap.</li>
                            <li>Masjid sebagai pusat kegiatan peribadatan. </li>
                        </ol>
                    </p>
                    <br>

                    <h4>Tujuan</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Malang memiliki tujuan dan pengetian yang dijabarkan berikut ini.
                        <ol>
                            <li>Menyelenggarakan pendidikan berdasarkan KTSP sehingga memiliki daya saing yang handal.</li>
                            <li>Menyelenggarakan pendidikan dengan berbasis pada akhlakul karimah dan ajaran salafuna as-sholihun (kitab kuning) sehingga mampu memiliki pemahaman pendidikan yang berlandasankan ajaran agama yang unggul.</li>
                            <li>Mengintegrasikan kurikulum nasional dan kurikulum pesantren sehingga mampu menyeimbangkan antara pendidikan formal dan pendidikan agama sebagai dasar landasan kehidupan. </li>
                            <li>Mengupayakan kecakapan dan kemampuan akademis yang terstandar nasional sehingga santri tetap dapat melanjutkan pendidikan di sekolah-sekolah lanjutan yang unggul.</li>
                            <li>Menyelenggarakan pendidikan dan pengembangan SDM dengan berbasis pada manajemen berbasis madrasah (MBM) sehingga pengawasan santri dapat dilakukan semaksimal mungkin.</li>
                            <li>Meningkatkan standar ketuntasan belajar dan kelulusan santri sehingga mampu bersaing dalam tingkat kelulusan.</li>
                        </ol>
                    </p>
                    <br>

                    <h4>Pengakuan dan Prestasi</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Madrasah Tsanawiyah (MTs) Darun Najah telah memiliki legalitas dan izin operasional sejak tahun 2013 dari Kementerian Agama Jawa Timur dan telah mendapatkan pengakuan dari Badan Akreditasi Nasional dengan nilai B+ pada tahun 2015. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Bahkan sejak berdiri hingga kini jumlah santri Madrasah Tsanawiyah Darun Najah dari tahun ke tahun juga semakin bertambah dan meningkat. Hal ini membuktikan bahwa MTs Darun Najah bukan saja diakui pemerintah, tetapi yang lebih penting juga diakui masyarakat luas sebagai madrasah yang bisa dipercaya dan diandalkan pendidikannya. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Berbagai prestasi juga telah ditorehkan oleh MTs Darun Najah, antara lain:
                        <ol>
                            <li>Juara umum Aksioma (Ajang Kreasi Seni dan Olahraga Madrasah) se KKM (Kelompok Kerja Madrasah) pada tahun 2013</li>
                            <li>Juara umum lomba antar sekolah se kecamatan Karangploso dalam rangka HUT 17 Agustus selama 2 tahun (1915 dan 2017)</li>
                            <li>Juara 2 olimpiade sains madrasah pada tahun 2016.</li>
                        </ol>
                    </p>
