                    <h4>Sejarah</h4>
                    <p>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pondok pesantren sebagai lembaga pendidikan tertua di Indonesia tentu memiliki karakter khas yang menjadi kekuatannya. Dan karakter khas serta kekuatan itu adalah pada penyelenggaraan pendidikannya yang berbasis pada ilmu agama yang bersumber dari al-Quran dan juga sunnah Rasulillah Saw.  <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada awalnya pondok pesantren di nusantara diselenggarakan dengan secara sederhana, dimana seorang Kyai yang menjadi pengasuh cukup membutuhkan tempat untuk mengaji, masjid untuk ibadah santri, dan asrama untuk tempat tinggal santri selama di pesantren. Sehingga pesantren akan disebut pesantren jika memiliki elemen (1) Kyai, (2) Santri, (3) Pondok atau asrama santri, (4) Masjid sebagai tempat ibadah dan tempat kyai mendidik santri. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Namun seiring perkembangan zaman, dengan semakin banyaknya santri yang tinggal di dalam pesantren tentu tidak mungkin seorang Kyai akan mengajar dan mendidik sendiri semua santri dengan berbagai jenjang dan level keilmuannya. Hingga Kyai dituntut untuk membuat sistem klasikal yang memungkinkan santri bisa belajar sesuai dengan kelas dan level kemampuan dengan dibantu oleh guru pembantu sebagai perpanjangan tangan dari Kyai. Begitupun dengan Pondok Pesantren Salafiyah PPAI Darun Najah yang dari tahun ke tahun makin meningkat jumlah kuantitas santrinya. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Pada awalnya, KH. Achmad Muchtar Ghozali selaku pendiri dan pengasuh pesantren PPAI Darun Najah mampu mengajar sendiri semua santrinya dari tahun 1967 hingga tahun 1981 dengan dibantu oleh beberapa santri senior. Namun seiring perkembangan jumlah santri yang makin meningkat banyak memaksa beliau untuk membuat kelas kelas untuk mendidik santri sesuai dengan level pendidikan dan kelas kemampuan santri. Sehingga sejak itulah program pendidikan Madrasah Diniyah Darun Najah mulai diberlakukan bagi semua santri.  <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sejak awal berdiri, Madrasah Diniyah Darun Najah sudah memiliki tiga jenjang pendidikan, yaitu jenjang Ibtida’iyah (Ula), Tsanawiyah (Wustho) dan juga Aliyah (Ulya) dimana masing-masing ditempuh selama 3 tahun. Namun sejak tahun 2012 dimana jarang sekali santri yang bisa menyelesaikan jenjang Madrasah Aliyah (MA) Diniyah karena mengikuti jenjang formal yang jika berkelanjutan hanya enam tahun memaksa pengasuh Pondok Pesantren Salafiyah PPAI Darun Najah untuk mengkaji ulang durasi pendidikan Madrasah Diniyah Darun Najah. <br>
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sehingga untuk memperpendek masa pendidikan Madin Darun Najah, Madrasah Ibtida’iyah diubah menjadi Madrasah I’dadiyah, yakni madrasah persiapan yang fokus untuk mempersiapkan santri agar mampu melanjutkan ke jenjang Madrasah Tsanawiyah Diniyah. Harapannya agar santri ketika masuk pesantren telah memasuki jenjang MTs formal akan bisa menyelesaikan program Madrasah Diniyahnya hingga ke Madrasah Aliyah Diniyah. Dan program itu berlangsung hingga sekarang.  <br>
                    </p>
                    <br>

